<richcontent TYPE="NOTE">
        <html>
          <head/>
          <body>
            <p>
      A SWOT analysis highlights the strengths and weaknesses within the business and identifies opportunities and threats in the external business environment. This is a very important analysis:
    </p>
            <ul>
              <li>
        Strengths are those that the company can exploit.
      </li>
              <li>
        Weaknesses are areas of the business that need to be addressed.
      </li>
              <li>
        Opportunities show the market place areas that can be built on.
      </li>
              <li>
        Threats are those issues that could affect the business.
      </li>
            </ul>
            <p>
      Not only should you list each of the Strengths, Weaknesses, etc., but also you should assess what each means to the shape, direction, and tactical running of the business.
    </p>
          </body>
